# 14. Print multiplication table of 24, 50 and 29 using loop.

table_24 = 24
table_29 = 29
table_50 = 50

print()
for i in range(1, 11):
    print(table_24, 'x', i, '=', table_24 * i)
print()
for i in range(1, 11):
    print(table_29, 'x', i, '=', table_29 * i)
print()
for i in range(1, 11):
    print(table_50, 'x', i, '=', table_50 * i)
